package com.company;

public class IfElse {
    public static void main(String[] args) {
        int number1 = 2;
        int number2 = 3;
        int number3 = 20;
        double number4 = 13.4d;

        if (number1 > number3) {
            System.out.println(number1 + " is greater than " + number3);
        } else {
            System.out.println(number1 + " is less than " + number3);
        }
    }
}
